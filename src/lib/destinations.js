// for mcq
export const getRandomCities = (currentCity, count) => {
  const otherCities = DESTINATIONS.filter((d) => d.city !== currentCity).map(
    (d) => d.city
  );
  const shuffled = [...otherCities].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getRandomDestination = () => {
  const randomIndex = Math.floor(Math.random() * DESTINATIONS.length);
  return DESTINATIONS[randomIndex];
};

export const getDestinationByCity = (city) => {
  return DESTINATIONS.find((d) => d.city === city);
};
